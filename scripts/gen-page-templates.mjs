// Turns the existing hand-built pages in src/pages into Studio starter templates.
//
// Each page is a thin composition of <SectionN/> blocks, so we parse the section
// imports + their render order and emit a Puck document per page. Output:
// src/builder/templates/generated.ts. Re-run: node scripts/gen-page-templates.mjs
//
// This realizes "save all existing pages section by section" — every page becomes
// an editable template you can start from in Studio.

import { readdirSync, readFileSync, writeFileSync, statSync, mkdirSync } from "node:fs";
import { dirname } from "node:path";
import { join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(fileURLToPath(new URL(".", import.meta.url)), "..");
const PAGES_DIR = join(ROOT, "src", "pages");
const OUT = join(ROOT, "src", "builder", "templates", "generated.ts");

// Seed sources are registered under custom types (manifests.tsx); everything else
// uses the generated type = source with non-alphanumerics replaced by "_".
// Seed sources are registered under custom (editable) types in manifests.tsx,
// which override the generated type for the same source. Parse those type/source
// pairs straight from the manifest so this can never drift out of sync.
const SEED_TYPE_BY_SOURCE = (() => {
  const src = readFileSync(join(ROOT, "src", "builder", "registry", "manifests.tsx"), "utf8");
  const map = {};
  // Match only top-level entries, where `source:` immediately follows `type:`.
  // (Field configs contain `type: "text"` etc., but never `type` then `source`.)
  const re = /\btype:\s*"([^"]+)",\s*source:\s*"([^"]+)"/g;
  let m;
  while ((m = re.exec(src))) map[m[2]] = m[1];
  return map;
})();

const typeForSource = (source) =>
  SEED_TYPE_BY_SOURCE[source] ?? source.replace(/[^a-zA-Z0-9]/g, "_");

const human = (s) =>
  s
    .replace(/Page$/, "")
    .replace(/([a-z])([A-Z0-9])/g, "$1 $2")
    .replace(/[-_]/g, " ")
    .trim();

const slugify = (s) =>
  s
    .replace(/Page$/, "")
    .replace(/([a-z])([A-Z0-9])/g, "$1-$2")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");

// Only top-level page files (skip dashboard/auth/onboarding/404 — no sections).
const SKIP = /^(Auth|Onboarding|NotFound|Coming)/;
const files = readdirSync(PAGES_DIR)
  .filter((f) => f.endsWith("Page.tsx") && !SKIP.test(f))
  .sort();

let genId = 0;
const templates = [];

for (const file of files) {
  const full = join(PAGES_DIR, file);
  if (statSync(full).isDirectory()) continue;
  const code = readFileSync(full, "utf8");

  // local import name -> section source path
  const importRe = /import\s+(\w+)\s+from\s+["']@\/shared\/sections\/([^"']+)["']/g;
  const sourceByName = {};
  let m;
  while ((m = importRe.exec(code))) sourceByName[m[1]] = m[2];
  if (Object.keys(sourceByName).length === 0) continue;

  // Render order: every <LocalName that is a known section import, in order.
  const body = code.slice(code.indexOf("return"));
  const tagRe = /<(\w+)[\s/>]/g;
  const content = [];
  let t;
  while ((t = tagRe.exec(body))) {
    const name = t[1];
    const source = sourceByName[name];
    if (!source) continue;
    const type = typeForSource(source);
    content.push({ type, props: { id: `${type}-${(genId++).toString(36)}` } });
  }
  if (content.length === 0) continue;

  const base = file.replace(/\.tsx$/, "");
  const name = human(base);
  templates.push({
    name,
    slug: slugify(base),
    sourcePage: `src/pages/${file}`,
    document: {
      root: { props: { title: name, headerStyle: 1, footerStyle: 1, noHeader: false, noFooter: false, mainClass: "bg-neutral-0" } },
      content,
      zones: {},
    },
  });
}

const out = `// AUTO-GENERATED by scripts/gen-page-templates.mjs — do not edit by hand.
// ${templates.length} starter templates derived from src/pages. Each is an editable
// Puck document referencing registered sections. Re-run the script to refresh.
import type { PageDocument } from "@/builder/types";

export type PageTemplate = { name: string; slug: string; sourcePage: string; document: PageDocument };

// Cast suppresses excess-property checks: our layout props (headerStyle, etc.)
// live on root.props, which Puck's Data types narrowly. They are valid at runtime.
export const PAGE_TEMPLATES = (${JSON.stringify(templates, null, 2)}) as unknown as PageTemplate[];
`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, out, "utf8");
console.log(`Wrote ${templates.length} templates -> ${relative(ROOT, OUT)}`);
