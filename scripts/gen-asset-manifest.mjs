// Scans public/assets/imgs and writes src/builder/assets.ts — the list the
// Studio image field's "Browse" grid offers. Re-run: node scripts/gen-asset-manifest.mjs

import { readdirSync, statSync, writeFileSync, mkdirSync } from "node:fs";
import { join, relative, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(fileURLToPath(new URL(".", import.meta.url)), "..");
const DIR = join(ROOT, "public", "assets", "imgs");
const OUT = join(ROOT, "src", "builder", "assets.ts");
const IMG = /\.(webp|jpg|jpeg|png|svg)$/i;

function walk(dir) {
  const out = [];
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    if (statSync(full).isDirectory()) out.push(...walk(full));
    else if (IMG.test(entry)) out.push(full);
  }
  return out;
}

const paths = walk(DIR)
  .map((f) => "/assets/imgs/" + relative(DIR, f).replace(/\\/g, "/"))
  .sort();

const out = `// AUTO-GENERATED by scripts/gen-asset-manifest.mjs — do not edit by hand.
// ${paths.length} images under public/assets/imgs, offered in the image field's Browse grid.
export const ASSET_IMAGES: string[] = ${JSON.stringify(paths)};
`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, out, "utf8");
console.log(`Wrote ${paths.length} asset paths -> ${relative(ROOT, OUT)}`);
