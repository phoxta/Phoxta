// Auto-extraction tooling for the Studio registry.
//
// Scans src/shared/sections for standalone section components and writes
// src/builder/registry/generated.tsx — a lazy-imported manifest of every
// section (render-as-is; editable fields are layered on in manifests.tsx).
// Re-run with: node scripts/gen-section-registry.mjs
//
// Lazy imports keep each section in its own chunk, so the published storefront
// only ships the sections a page actually uses.

import { readdirSync, readFileSync, writeFileSync, statSync } from "node:fs";
import { join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(fileURLToPath(new URL(".", import.meta.url)), "..");
const SECTIONS_DIR = join(ROOT, "src", "shared", "sections");
const OUT = join(ROOT, "src", "builder", "registry", "generated.tsx");

/** Recursively collect .tsx files. */
function walk(dir) {
  const out = [];
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    if (statSync(full).isDirectory()) out.push(...walk(full));
    else if (entry.endsWith(".tsx")) out.push(full);
  }
  return out;
}

/** Broad palette category from the section's top folder. */
function categoryOf(folder) {
  if (folder.startsWith("index-")) return "Home sections";
  if (folder.startsWith("about")) return "About";
  if (folder.startsWith("portfolio")) return "Portfolio";
  if (folder.startsWith("services")) return "Services";
  if (folder.startsWith("product")) return "Commerce";
  if (folder.startsWith("team")) return "Team";
  if (folder.startsWith("archive") || folder.startsWith("blog")) return "Blog & Archive";
  if (folder.startsWith("careers")) return "Careers";
  if (folder.startsWith("faqs")) return "FAQ";
  if (folder.startsWith("contact")) return "Contact";
  return "Other";
}

const human = (s) =>
  s
    .split(/[-_]/)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join(" ");

const files = walk(SECTIONS_DIR).sort();
const entries = [];

for (const file of files) {
  const rel = relative(SECTIONS_DIR, file).replace(/\\/g, "/"); // e.g. about-1/Section1.tsx
  const base = rel.split("/").pop().replace(/\.tsx$/, "");
  const source = rel.replace(/\.tsx$/, "");

  // Only standalone section components: basename must start with "Section",
  // no spaces in the path (stray files), and not a server-only variant.
  if (/\s/.test(rel)) continue;
  if (!base.startsWith("Section")) continue;
  if (/Server$/.test(base)) continue;

  const code = readFileSync(file, "utf8");
  if (!/export\s+default/.test(code)) continue;

  const folder = source.split("/")[0];
  const type = source.replace(/[^a-zA-Z0-9]/g, "_");
  const label = `${human(folder)}: ${base.replace(/[-_]/g, " ")}`;
  entries.push({ type, source, label, group: categoryOf(folder) });
}

const importLines = entries
  .map((e, i) => `import C${i} from "@/shared/sections/${e.source}";`)
  .join("\n");

const manifestLines = entries
  .map(
    (e, i) =>
      `  { type: ${JSON.stringify(e.type)}, source: ${JSON.stringify(e.source)}, label: ${JSON.stringify(
        e.label,
      )}, group: ${JSON.stringify(e.group)}, component: C${i} as unknown as ComponentType<Record<string, unknown>> },`,
  )
  .join("\n");

const out = `// AUTO-GENERATED by scripts/gen-section-registry.mjs — do not edit by hand.
// ${entries.length} sections registered render-as-is. Editable fields are layered
// on in manifests.tsx (which overrides these by \`source\`).
// Static imports (not lazy import()) on purpose: 200+ dynamic-import chunks OOM
// Vercel's build, and per-chunk grouping broke React's JSX runtime. The registry
// only loads in lazy routes (Studio/preview/published), never the marketing entry.
import type { ComponentType } from "react";
import type { SectionManifest } from "./types";

${importLines}

export const GENERATED_MANIFESTS: SectionManifest[] = [
${manifestLines}
];
`;

writeFileSync(OUT, out, "utf8");
console.log(`Wrote ${entries.length} sections -> ${relative(ROOT, OUT)}`);
